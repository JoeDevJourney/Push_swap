#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (s)
	{
		while (*s)
			write(fd, s++, 1);
		write(fd, "\n", 1);
	}
}
//putting an endline at the end of the character.
//if s is there to have a safety check against NULL.
//it goes through every single character of s and then writes down
//an endline at the end.